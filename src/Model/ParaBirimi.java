package Model;



public class ParaBirimi {

	
	private String name;

	public ParaBirimi() {}
	public ParaBirimi(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	public String toString() {
		return this.name;
	}
	
	
	
}
